#create a function max_two with two prameters num1,num2 return largest number

def max_two(num1,num2):

    return num1 if num1>num2 else num2

print(max_two(25,35))


#create a function min_two with two prameters num1,num2 return smallest number


def min_two(num1,num2):

    return num1 if num1<num2 else num2

print(min_two(25,35))
